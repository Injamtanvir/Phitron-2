#include<bits/stdc++.h>
using namespace std;
int main(){
    long long int a, b;
    cin >> a >> b;
    long long int plus = a + b;
    long long multiplication = a * b;
    long long int Minus = a -b;

    cout << a << " + " << b << " = " << plus << endl;
    cout << a << " * " << b << " = " << multiplication << endl;
    cout << a << " - " << b << " = " << Minus << endl;

    return 0;
 
}